CREATE TABLE IF NOT EXISTS kunde.kunde (
    id        INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1000) PRIMARY KEY,
    nachname  TEXT NOT NULL,
    email     TEXT NOT NULL UNIQUE,
    username  TEXT UNIQUE,
    version   INTEGER NOT NULL DEFAULT 0
);

CREATE TABLE IF NOT EXISTS kunde.adresse (
    id          INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1000) PRIMARY KEY,
    strasse     TEXT NOT NULL,
    hausnummer  TEXT NOT NULL,
    plz         TEXT NOT NULL CHECK (plz ~ '^\d{5}$'),
    ort         TEXT NOT NULL,
    kunde_id    INTEGER NOT NULL UNIQUE REFERENCES kunde.kunde ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS kunde.bestellung (
    id           INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1000) PRIMARY KEY,
    produktname  TEXT NOT NULL,
    menge        INTEGER NOT NULL CHECK (menge > 0),
    kunde_id     INTEGER NOT NULL REFERENCES kunde.kunde ON DELETE CASCADE
);
